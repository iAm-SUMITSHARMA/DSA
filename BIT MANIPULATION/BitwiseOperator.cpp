#include <iostream>
using namespace std;
int main()
{
    // Bitwise And (&)
    cout << (3 & 5) << endl; //=> 1
    // 3 in binary = 011
    // 5 in binary = 101

    // ---------------------------------------

    // Bitwise OR (|)
    cout << (3 | 5) << endl; //=> 7

    // ---------------------------------------

    // Bitwise XOR (^)
    cout << (3 ^ 5) << endl; //=> 6
}